#### 
# Make sure transformations map to the proper nodes
#### 

function transformation_test(el)
    @testset "Transformations for order $(el.order) $(Base.typename(typeof(el)).wrapper)" begin
        for i=1:el.n
            @test transform_to_ref_el(el, el.p[i, :], el.p) == el.p[i, :]
            @test transform_from_ref_el(el, el.p[i, :], el.p) == el.p[i, :]
        end
    end
end
for el_type=[Line, Triangle, Wedge]
    for order=1:2
        transformation_test(el_type(order=order))
    end
end