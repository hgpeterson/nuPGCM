"""
    integral = trapz(f, x)

Integrate array `f` over domain `x` using trapezoidal rule.
"""
function trapz(f, x)
    return 0.5*dot((f[1:end-1] .+ f[2:end]), (x[2:end] .- x[1:end-1]))
end

"""
    integral = cumtrapz(f, x)

Cumulatively integrate array `f` over domain `x` using trapezoidal rule.
"""
function cumtrapz(f, x)
    n = size(f, 1)
    y = zeros(n)
    for i=2:n
        y[i] = y[i-1] + 0.5*(f[i] + f[i-1])*(x[i] - x[i-1])
    end
    return y
end

"""
    integral = ref_el_quad(f, wts, pts)

Compute integral of `f` over the reference element. Quadrature rule defined by weights `wts`
and integration points `pts`. (See `quad_weights_points`)
"""
function ref_el_quad(f, wts, pts)
    return sum(wts[i]*f(pts[i, :]) for i ∈ eachindex(wts))
end

