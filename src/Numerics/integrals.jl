"""
    integral = trapz(f, x)

Integrate array `f` over domain `x` using trapezoidal rule.
"""
function trapz(f, x)
    return 0.5*dot((f[1:end-1] .+ f[2:end]), (x[2:end] .- x[1:end-1]))
end

"""
    integral = cumtrapz(f, x)

Cumulatively integrate array `f` over domain `x` using trapezoidal rule.
"""
function cumtrapz(f, x)
    n = size(f, 1)
    y = zeros(n)
    for i=2:n
        y[i] = y[i-1] + 0.5*(f[i] + f[i-1])*(x[i] - x[i-1])
    end
    return y
end

"""
    integral = std_tri_quad(f, w, ξ)

Compute ∫ f(ξ) dξ over a standard triangle quadrature rule defined by weights `w`
and integration points `ξ`.
"""
function std_tri_quad(f, w, ξ)
    # sum contributions and multiply by area of standard triangle (1/2)
    return 1/2*dot(w, [f(ξ[i, :]) for i in axes(ξ, 1)])
end

